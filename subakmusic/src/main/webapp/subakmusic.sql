	"NUM" NUMBER(7,0) NOT NULL ENABLE, -- 게시물 번호
	"WRITER" VARCHAR2(12) NOT NULL ENABLE, -- 작성자
	"EMAIL" VARCHAR2(30), -- 이메일
	"SUBJECT" VARCHAR2(150) NOT NULL ENABLE, -- 제목
	"PASS" VARCHAR2(10), -- 비밀번호
	"READCOUNT" NUMBER(5,0) DEFAULT 0 NOT NULL ENABLE, -- 조회수
	"REGDATE" DATE DEFAULT SYSDATE NOT NULL ENABLE, -- 등록일
	"CONTENT" VARCHAR2(4000) NOT NULL ENABLE, -- 내용
	CONSTRAINT "NOTICE_PK" PRIMARY KEY ("NUM") ENABLE
);

CREATE SEQUENCE "NOTICE_SEQ"
START WITH 1 INCREMENT BY 1 MINVALUE 1 MAXVALUE 99999
NOCACHE NOCYCLE NOORDER;



