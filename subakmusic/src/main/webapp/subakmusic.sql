----- 회원정보

CREATE TABLE "MEMBER" (
	"ID" VARCHAR2(20) NOT NULL ENABLE, -- 회원아이디
	"NAME" VARCHAR2(20) NOT NULL ENABLE, -- 회원이름
	"PASS" VARCHAR2(20) NOT NULL ENABLE, -- 비밀번호
	"PHONE1" VARCHAR2(5) NOT NULL ENABLE, -- 폰번호1
	"PHONE2" VARCHAR2(5) NOT NULL ENABLE, -- 폰번호2
	"PHONE3" VARCHAR2(5) NOT NULL ENABLE, -- 폰번호3
	"EMAIL" VARCHAR2(50) NOT NULL ENABLE, -- 이메일주소
	"BIRTH1" NUMBER(4,0) NOT NULL ENABLE, -- 생년
	"BIRTH2" NUMBER(2,0) NOT NULL ENABLE, -- 생월
	"BIRTH3" NUMBER(2,0) NOT NULL ENABLE, -- 생일
	"F_SINGER" VARCHAR2(500), -- 좋아하는가수 (콤마[,]로 구분)
	"F_GENRE" VARCHAR2(500), -- 좋아하는장르 (콤마[,]로 구분)
	"POINT" NUMBER(5,0) DEFAULT 0 NOT NULL ENABLE, -- 회원보유포인트
	"REGDATE" DATE DEFAULT SYSDATE NOT NULL ENABLE, -- 회원가입일자
	CONSTRAINT "MEMBER_PK" PRIMARY KEY ("ID") ENABLE
);



