package subak.albuminfor.model;

public class GetSingerAndAlbumVO {

	/**
	 * @uml.property  name="singer"
	 */
	private String singer;
	/**
	 * @uml.property  name="album"
	 */
	private String album;
	
	/**
	 * @return
	 * @uml.property  name="singer"
	 */
	public String getSinger() {
		return singer;
	}
	/**
	 * @param singer
	 * @uml.property  name="singer"
	 */
	public void setSinger(String singer) {
		this.singer = singer;
	}
	/**
	 * @return
	 * @uml.property  name="album"
	 */
	public String getAlbum() {
		return album;
	}
	/**
	 * @param album
	 * @uml.property  name="album"
	 */
	public void setAlbum(String album) {
		this.album = album;
	}
	
	
}
