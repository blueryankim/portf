package hooray.myinformation.model;

public class ProjectVO {
	
	private int projectNum;
	private String maintitle;
	private int currentpeople;
	private int limitpeople;
	private int leavedate;
	private int onchattingpeople;
	private int unreadcount;
	
	public int getProjectNum() {
		return projectNum;
	}
	public void setProjectNum(int projectNum) {
		this.projectNum = projectNum;
	}
	public String getMaintitle() {
		return maintitle;
	}
	public void setMaintitle(String maintitle) {
		this.maintitle = maintitle;
	}
	public int getCurrentpeople() {
		return currentpeople;
	}
	public void setCurrentpeople(int currentpeople) {
		this.currentpeople = currentpeople;
	}
	public int getLimitpeople() {
		return limitpeople;
	}
	public void setLimitpeople(int limitpeople) {
		this.limitpeople = limitpeople;
	}
	public int getLeavedate() {
		return leavedate;
	}
	public void setLeavedate(int leavedate) {
		this.leavedate = leavedate;
	}
	public int getOnchattingpeople() {
		return onchattingpeople;
	}
	public void setOnchattingpeople(int onchattingpeople) {
		this.onchattingpeople = onchattingpeople;
	}
	public int getUnreadcount() {
		return unreadcount;
	}
	public void setUnreadcount(int unreadcount) {
		this.unreadcount = unreadcount;
	}
	
	

}
